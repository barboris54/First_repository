# Словарь в языке Python представляет собой совокупность пар «ключ-значение».
# Каждый ключ связывается с некоторым значением, и программа может получить
# значение, связанное с заданным ключом.

alien = {'colour' : 'green', 'points' : 5}
print(alien['colour'])  # Чтобы получить значение, связанное с ключом, укажите имя словаря, а затем ключ
                        #в квадратных скобках
print((alien['points']))