# Удаление
# Для удаления элементов во множестве используется метод discard
a = {54,24,53,4,6,7}
a. discard(6)

print (a)

# Для удаления можно использовать метод remove
b = {11,23,44,56}
b.remove(44)

print(b)

# Для удаления случайного элемента из множества подойдет метод pop
c = {22,678,21,534}
c.pop()

print(c)